public class EX06_증감연산자 {
    public static void main(String[] args) {
        int k =100;
        k+=1;
        System.out.println(k);
        k++;
        System.out.println(k);
        ++k;
        System.out.println(k);

        int l =100;
        l -=1;
        System.out.println(l);
        l--;
        System.out.println(l);
        --l;
        System.out.println(l);
    }
}
